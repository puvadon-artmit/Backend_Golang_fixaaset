func GetById(c *fiber.Ctx) error {
	value, err := ServicesRound_Count.GetById(c.Params("round_count_id"))
	if err != nil {
		return c.Status(404).JSON(fiber.Map{
			"status": "error",
			"error":  err,
		})
	}
	return c.JSON(fiber.Map{
		"status": "success",
		"result": value,
	})
}

